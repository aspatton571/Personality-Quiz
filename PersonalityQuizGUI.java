    private void displayQuestion(PersonalityQuestion question) {
        questionLabel.setText(question.getQuestionText());
        choice1Button.setText(question.getChoices()[0]);
        choice2Button.setText(question.getChoices()[1]);
        choice3Button.setText(question.getChoices()[2]);
        buttonGroup1.clearSelection();
    }

    private void showResults() {
        int introvertPoints = 0;
        int extrovertPoints = 0;

        for (PersonalityAnswer answer : answers) {
            int points = answer.getSelectedPoints();
            if (points < 0) introvertPoints += Math.abs(points);
            else extrovertPoints += points;
        }

        PersonalityCompute compute = new PersonalityCompute(introvertPoints, extrovertPoints);
        JOptionPane.showMessageDialog(this, compute.getResult());
    }


