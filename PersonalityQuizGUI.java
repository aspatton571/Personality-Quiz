    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if (currentQuestionIndex < quiz.getQuestions().size()) {
            PersonalityQuestion question = quiz.getQuestions().get(currentQuestionIndex);
            int selected = -1;

            if (choice1Button.isSelected()) selected = 0;
            else if (choice2Button.isSelected()) selected = 1;
            else if (choice3Button.isSelected()) selected = 2;

            if (selected == -1) {
                JOptionPane.showMessageDialog(this, "Please select an option.");
                return;
            }

            answers.add(new PersonalityAnswer(question, selected));
            currentQuestionIndex++;

            if (currentQuestionIndex < quiz.getQuestions().size()) {
                displayQuestion(quiz.getQuestions().get(currentQuestionIndex));
            }
            else {
                showResults();
            }

        }

    }

